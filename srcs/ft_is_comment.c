#include "lemin.h"

int		ft_is_comment(char *line)
{
	if (ft_strlen(line) >= 2)
	{
		if (line[0] == '#' && line[1] != '#')
			return (1);
	}
	if (ft_strlen(line) >= 3)
	{
		if (line[0] == '#' && line[1] == '#' && line[2] == '#')
			return (1);
	}
	return (0);
}
