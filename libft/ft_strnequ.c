#include <string.h>

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	i = 0;
	while (i < n && (*s1 || *s2))
	{
		if ((*s1 - *s2) != 0)
			return (0);
		s1++;
		s2++;
		i++;
	}
	return (1);
}
